public class DataTypeTest {
	public static void main(String[] args) {
	
		//		정수형
		byte num1 = 1; // Max 127
		short num2 = 2; // Max 32767
		int num3 = 3; // Max 2147483647
		long num4 = 4L; // Max 922337203685477507L
		
		//		실수형
		float var1 = 0.123456789f; // 7번째 자리까지 출력
		double var2 = 0.1234567890123456789; // 16자리 까지 출력
		
		System.out.println("var1 : " + var1);
		System.out.println("var2 : " + var2);
		//		논리형
		boolean data1 = true;
		boolean data2 = false;
		
		//		문자형
		char c1 = 'A';
		char c2 = '가';
		//		문자열
		String str1 = "ㅎㅇ";
		String str2 = "Apple";
		
		
		// 자료형(Data Type)
		// 자료형은 기본형과 참조형(객체)으로 나뉨
	}
}
